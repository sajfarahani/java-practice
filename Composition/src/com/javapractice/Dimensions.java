public class Dimensions {
    private int width;
    private int heigh;
    private int depth;

    public Dimensions(int width, int heigh, int depth) {
        this.width = width;
        this.heigh = heigh;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getHeigh() {
        return heigh;
    }

    public int getDepth() {
        return depth;
    }
}
